document.addEventListener("DOMContentLoaded", function () {
    const tabRadios = document.querySelectorAll('input[name="tab-control"]');
    const video = document.querySelector('.aiDemoVideoStart');

    function controlVideoPlayback() {
        const tab4 = document.getElementById('tab3');
        if (tab4.checked) {
            video.play();
        } else {
            video.pause();
        }
    }

    tabRadios.forEach(radio => {
        radio.addEventListener('change', controlVideoPlayback);
    });

    controlVideoPlayback(); // Run on load in case tab4 is default
});
